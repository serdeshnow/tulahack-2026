from __future__ import annotations

from typing import Any


def _word(
    text: str,
    start_ms: int,
    end_ms: int,
    *,
    is_redacted: bool = False,
    redaction_reason: str | None = None,
) -> dict[str, Any]:
    payload: dict[str, Any] = {
        "text": text,
        "start_ms": start_ms,
        "end_ms": end_ms,
    }
    if is_redacted:
        payload["is_redacted"] = True
    if redaction_reason is not None:
        payload["redaction_reason"] = redaction_reason
    return payload


def _segment(
    text: str,
    start_ms: int,
    end_ms: int,
    words: list[dict[str, Any]],
    *,
    segment_id: str,
    speaker_id: str = "spk_0",
) -> dict[str, Any]:
    return {
        "segment_id": segment_id,
        "speaker_id": speaker_id,
        "start_ms": start_ms,
        "end_ms": end_ms,
        "text": text,
        "words": words,
    }


def _transcript(
    full_text: str,
    canonical_segments: list[dict[str, Any]],
    segments: list[dict[str, Any]],
    *,
    variant: str,
) -> dict[str, Any]:
    return {
        "format": "json",
        "language": "ru",
        "variant": variant,
        "full_text": full_text,
        "canonical_segments": canonical_segments,
        "segments": segments,
    }


